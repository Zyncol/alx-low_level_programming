#include "3-calc.h"
/**
*op_add - adds two integers
*@a: first number
*@b: second number
*Return: the sum
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
*op_sub - subtract two integers
*@a: first int
*@b: second int
*Return: the difference
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
*op_mul - multiplies two numbers
*@a: first integer
*@b: second integer
*Return: the product
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
*op_div - divide integers
*@a: first nuumber
*@b: second number
*Return: the quotient
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
*op_mod - gets the remainder
*@a: first integer
*@b: second integer
*Return: the remainder
*/
int op_mod(int a, int b)
{
return (a % b);
}
